# คำนวณความชันแนว X และ Y ของ ny_map
sobel_x_ny = cv2.Sobel(ny_blur, cv2.CV_64F, 1, 0, ksize=3)
sobel_y_ny = cv2.Sobel(ny_blur, cv2.CV_64F, 0, 1, ksize=3)
mag_ny = cv2.magnitude(sobel_x_ny, sobel_y_ny)

# รวมพลังงานความชัน (Gradient Energy)
grad_x = cv2.convertScaleAbs(mag_nx)
grad_y = cv2.convertScaleAbs(mag_ny)

# ใช้ชื่อตัวแปร 'curvature' เหมือนเดิม เพื่อให้ส่งค่าไปรันกราฟ 3D ใน Cell 6 ได้ทันทีโดยไม่ต้องแก้โค้ดเพิ่ม
