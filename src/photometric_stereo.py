for n_est in n_estimators_list:
    for m_samp in max_samples_list:
        # contamination="auto" หมายถึง ปล่อยให้มันเดาเอง (มักจะเวิร์กสุดถ้าเราเทรนด้วยของดีล้วน)
        temp_model = IsolationForest(n_estimators=n_est, max_samples=m_samp, contamination='auto', random_state=42)
        temp_model.fit(X_train_scaled)

        # IsolationForest คืนค่าลบเมื่อเป็น Anomaly เราจึงต้องคูณ -1 เพื่อให้คะแนนของเสียสูงกว่าของดี (เพื่อหา AUROC)
