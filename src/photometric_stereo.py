import os
import glob
import cv2
import torch
import numpy as np
import torch.nn as nn
import torch.optim as optim
import torch.nn.functional as F
import matplotlib.pyplot as plt
from PIL import Image
from torchvision import models, transforms
from torch.utils.data import Dataset, DataLoader
from sklearn.metrics import roc_auc_score, precision_recall_curve

print("🚀 เริ่มต้นสถาปัตยกรรมระดับโลก: Teacher-Student Distillation (EfficientAD Concept)")

# ==========================================
# 1. การตั้งค่าระบบ (Configuration)
# ==========================================
DATA_ROOT = "/content/drive/MyDrive/IAD/dataset/mvtec_dataset"
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
IMAGE_SIZE = 256
BATCH_SIZE = 16
EPOCHS = 50          # จำนวนรอบในการสอนนักเรียน (ปรับเพิ่มได้ถ้าอยากให้แม่นขึ้น)
LEARNING_RATE = 1e-3
print(f"🖥️ ใช้งานอุปกรณ์: {DEVICE}")

# ==========================================
# 2. การจัดการ Dataset
# ==========================================
transform = transforms.Compose([
    transforms.Resize((IMAGE_SIZE, IMAGE_SIZE)),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
])

class AnomalyDataset(Dataset):
    def __init__(self, folder_path, label=0):
        self.paths = sorted(glob.glob(os.path.join(folder_path, "*.png")))
        self.label = label
    def __len__(self): return len(self.paths)
    def __getitem__(self, idx):
        img_path = self.paths[idx]
        img = Image.open(img_path).convert("RGB")
        return transform(img), self.label, img_path

train_dataset = AnomalyDataset(os.path.join(DATA_ROOT, "train", "good"), label=0)
train_loader = DataLoader(train_dataset, batch_size=BATCH_SIZE, shuffle=True)

# ==========================================
# 3. สร้างโมเดล ครู (Teacher) และ นักเรียน (Student)
# ==========================================
print("👨‍🏫 โหลดโมเดล Teacher (เก่งแล้ว) และ Student (เริ่มต้นจากศูนย์)...")
# คุณครู: มีความรู้จาก ImageNet และถูกแช่แข็ง (Freeze) ไม่ให้เรียนรู้เพิ่ม
teacher = models.resnet18(weights=models.ResNet18_Weights.IMAGENET1K_V1).to(DEVICE)
teacher.eval()
for param in teacher.parameters():
    param.requires_grad = False

# นักเรียน: โครงสร้างเหมือนครู แต่ยังไม่มีความรู้ (Random Weights)
student = models.resnet18(weights=None).to(DEVICE)
student.train()

# สร้าง "สายสอดแนม" เพื่อดึงข้อมูลจากสมองส่วน Layer 1, 2, 3
t_features, s_features = {}, {}

def get_t_hook(name):
    def hook(model, input, output): t_features[name] = output
    return hook

def get_s_hook(name):
    def hook(model, input, output): s_features[name] = output
    return hook

for name, layer in zip(['layer1', 'layer2', 'layer3'], [teacher.layer1, teacher.layer2, teacher.layer3]):
    layer.register_forward_hook(get_t_hook(name))

for name, layer in zip(['layer1', 'layer2', 'layer3'], [student.layer1, student.layer2, student.layer3]):
    layer.register_forward_hook(get_s_hook(name))

# ==========================================
# 4. ฝึกสอนนักเรียน (Training Phase)
# ==========================================
optimizer = optim.Adam(student.parameters(), lr=LEARNING_RATE)
criterion = nn.MSELoss() # ใช้วัดความต่างระหว่างครูกับนักเรียน

print(f"🧠 กำลังให้นักเรียนเรียนรู้พื้นผิวปกติจากคุณครู (Training {EPOCHS} Epochs)...")
for epoch in range(EPOCHS):
    total_loss = 0
    for imgs, _, _ in train_loader:
        imgs = imgs.to(DEVICE)

        optimizer.zero_grad()

        # ให้ครูและนักเรียนดูภาพเดียวกัน
        _ = teacher(imgs)
        _ = student(imgs)

        # เปรียบเทียบสมอง (Feature Maps) ในแต่ละชั้น
        loss = 0
        for layer in ['layer1', 'layer2', 'layer3']:
            # บังคับให้นักเรียน (s) เลียนแบบครู (t)
            loss += criterion(s_features[layer], t_features[layer])

        loss.backward()
        optimizer.step()
        total_loss += loss.item()

    if (epoch+1) % 5 == 0 or epoch == 0:
        print(f"   Epoch [{epoch+1}/{EPOCHS}] | Distillation Loss: {total_loss/len(train_loader):.4f}")

print("✅ การฝึกสอนเสร็จสมบูรณ์!")

# ==========================================
# 5. การทดสอบและจับผิดของเสีย (Inference)
# ==========================================
