import cv2
import torch
import numpy as np
import torch.nn.functional as F
import matplotlib.pyplot as plt
from PIL import Image
from torchvision import models, transforms
from torch.utils.data import Dataset, DataLoader
from sklearn.metrics import roc_auc_score, precision_recall_curve, roc_curve

print("🚀 เริ่มต้นสถาปัตยกรรมระดับโลก: Deep Feature Memory Bank (PatchCore Concept)")

# ==========================================
# 1. การตั้งค่าระบบ (Configuration)
# ==========================================
DATA_ROOT = "/content/drive/MyDrive/IAD/dataset/mvtec_dataset"
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")
IMAGE_SIZE = 256        # ย่อภาพลงเพื่อให้ GPU ทำงานได้ไวขึ้น
CORESET_RATIO = 0.05    # สร้างธนาคารความจำ (Memory Bank) โดยเก็บฟีเจอร์แค่ 5% ของทั้งหมด
print(f"🖥️ ใช้งานอุปกรณ์: {DEVICE}")

# ==========================================
# 2. เตรียม "ดวงตาที่มองเห็นความลึก" (WideResNet50)
# ==========================================
# ใช้ WideResNet50 เพราะเป็นสถาปัตยกรรมที่ดีที่สุดในเปเปอร์ PatchCore
print("📥 โหลดโมเดล WideResNet50...")
model = models.wide_resnet50_2(weights=models.Wide_ResNet50_2_Weights.IMAGENET1K_V1).to(DEVICE)
model.eval()

# สร้าง "สายลับ" ดึงข้อมูลจาก Layer 2 และ 3 (ซึ่งเป็นชั้นที่เก็บลักษณะพื้นผิวได้ดีที่สุด)
features = {}
def get_features(name):
    def hook(model, input, output):
        features[name] = output.detach()
    return hook

model.layer2.register_forward_hook(get_features('layer2'))
model.layer3.register_forward_hook(get_features('layer3'))

# ==========================================
# 3. การจัดการ Dataset
# ==========================================
transform = transforms.Compose([
    transforms.Resize((IMAGE_SIZE, IMAGE_SIZE)),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
])

class AnomalyDataset(Dataset):
    def __init__(self, folder_path, label=0):
        self.paths = sorted(glob.glob(os.path.join(folder_path, "*.png")))
        self.label = label
    def __len__(self): return len(self.paths)
    def __getitem__(self, idx):
        img_path = self.paths[idx]
        img = Image.open(img_path).convert("RGB")
        return transform(img), self.label, img_path

train_loader = DataLoader(AnomalyDataset(os.path.join(DATA_ROOT, "train", "good"), label=0), batch_size=8, shuffle=False)

# ==========================================
# 4. สร้างธนาคารความจำ (Memory Bank Construction)
# ==========================================
print("🧠 กำลังสกัดฟีเจอร์และสร้าง Memory Bank จากภาพชิ้นงานปกติ...")
train_features = []

with torch.no_grad():
    for imgs, _, _ in train_loader:
        _ = model(imgs.to(DEVICE))

        # ดึงฟีเจอร์ออกมา
        feat2 = features['layer2'] # (Batch, 512, 32, 32)
        feat3 = features['layer3'] # (Batch, 1024, 16, 16)

        # ขยาย Layer 3 ให้เท่ากับ Layer 2 และนำมาต่อกัน (Concatenate)
        feat3 = F.interpolate(feat3, size=feat2.shape[-2:], mode='bilinear', align_corners=False)
        patch_feats = torch.cat([feat2, feat3], dim=1) # กลายเป็น (Batch, 1536, 32, 32)

        # กรองให้เนียนขึ้นด้วย Average Pooling (3x3)
        patch_feats = F.avg_pool2d(patch_feats, 3, 1, 1)

        # แบนราบและนำไปรวมในกล่องเก็บฟีเจอร์
        B, C, H, W = patch_feats.shape
        patch_feats = patch_feats.permute(0, 2, 3, 1).reshape(-1, C) # (Batch*H*W, 1536)
        train_features.append(patch_feats.cpu())

train_features = torch.cat(train_features, dim=0) # รวมฟีเจอร์ทั้งหมด

# สุ่มเก็บฟีเจอร์แค่ 5% (Coreset Subsampling) เพื่อไม่ให้ล้น RAM
num_feats = train_features.shape[0]
subset_size = int(num_feats * CORESET_RATIO)
indices = torch.randperm(num_feats)[:subset_size]
memory_bank = train_features[indices].to(DEVICE) # นำขึ้น GPU เพื่อความรวดเร็วตอน Test

print(f"📦 สร้าง Memory Bank เสร็จสิ้น! (เก็บไว้ {subset_size:,} แพทช์อ้างอิง)")

# ==========================================
# 5. การทดสอบและจับผิดของเสีย (Inference)
# ==========================================
print("🔍 กำลังค้นหาของเสียในชุด Test Set...")
test_good = AnomalyDataset(os.path.join(DATA_ROOT, "test", "good"), label=0)

test_defects = []
for d in [f.path for f in os.scandir(os.path.join(DATA_ROOT, "test")) if f.is_dir() and f.name != "good"]:
    test_defects.append(AnomalyDataset(d, label=1))

test_dataset = torch.utils.data.ConcatDataset([test_good] + test_defects)
test_loader = DataLoader(test_dataset, batch_size=1, shuffle=False) # รันทีละรูปเพื่อให้สกัด Heatmap ง่าย

y_true, image_scores, heatmaps, img_paths_list = [], [], [], []

with torch.no_grad():
    for img, label, path in test_loader:
        _ = model(img.to(DEVICE))

        feat2 = features['layer2']
        feat3 = F.interpolate(features['layer3'], size=feat2.shape[-2:], mode='bilinear', align_corners=False)
        patch_feats = F.avg_pool2d(torch.cat([feat2, feat3], dim=1), 3, 1, 1)

        B, C, H, W = patch_feats.shape
        patch_feats = patch_feats.permute(0, 2, 3, 1).reshape(-1, C) # (1024, 1536)

        # คำนวณระยะห่าง (L2 Distance) ระหว่าง Test Patch กับ Memory Bank
        distances = torch.cdist(patch_feats, memory_bank) # วัดความต่าง
        min_distances, _ = torch.min(distances, dim=1)    # หาอันที่เหมือนคนปกติที่สุด

        # ถ้าระยะห่างมาก = ผิดปกติมาก!
        img_score = torch.max(min_distances).item()
        image_scores.append(img_score)
        y_true.append(label.item())
        img_paths_list.append(path[0])

        # นำค่าระยะห่างมาสร้างเป็น Heatmap
        anomaly_map = min_distances.reshape(1, 1, H, W)
        anomaly_map = F.interpolate(anomaly_map, size=(IMAGE_SIZE, IMAGE_SIZE), mode='bilinear', align_corners=False)
        heatmaps.append(anomaly_map.squeeze().cpu().numpy())

# ==========================================
# 6. ประเมินผลระดับ SOTA (Evaluation)
# ==========================================
auroc = roc_auc_score(y_true, image_scores)
precisions, recalls, thresholds = precision_recall_curve(y_true, image_scores)
f1_scores = (2 * precisions * recalls) / (precisions + recalls + 1e-10)
best_idx = np.argmax(f1_scores)
best_threshold = thresholds[best_idx]

print("\n" + "="*50)
print(f"🌟 [FINAL RESULTS] Deep Feature Memory Bank (PatchCore)")
print(f"🥇 AUROC:          {auroc:.4f}")
print(f"🎯 Optimal Thresh: {best_threshold:.4f}")
print(f"📊 F1-Score:       {f1_scores[best_idx]:.4f} (Precision: {precisions[best_idx]:.2f} | Recall: {recalls[best_idx]:.2f})")
print("="*50 + "\n")

