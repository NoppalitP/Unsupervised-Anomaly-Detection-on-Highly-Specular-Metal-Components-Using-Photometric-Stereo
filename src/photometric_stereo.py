class AffineCouplingBlock(nn.Module):
    def __init__(self, channels):
        super().__init__()
        self.split_len = channels // 2
        # เครือข่ายคำนวณหา Scale (s) และ Translation (t)
        self.net = nn.Sequential(
            nn.Conv2d(self.split_len, channels, kernel_size=3, padding=1),
            nn.ReLU(inplace=True),
            nn.Conv2d(channels, channels, kernel_size=3, padding=1)
        )
        # เคล็ดลับ: กำหนดให้น้ำหนักเริ่มต้นเป็น 0 เพื่อให้โมเดลเริ่มจาก Identity Transform
        nn.init.zeros_(self.net[-1].weight)
        nn.init.zeros_(self.net[-1].bias)

    def forward(self, x):
        x1, x2 = x[:, :self.split_len], x[:, self.split_len:]
        st = self.net(x1)
        s, t = st[:, :self.split_len], st[:, self.split_len:]

        s = torch.clamp(s, -5., 5.) # ป้องกัน Gradients ระเบิด
        y2 = x2 * torch.exp(s) + t  # สมการ Affine Coupling
        log_det = torch.sum(s, dim=1, keepdim=True)

        # นำมาต่อกันแล้วสลับตำแหน่ง (Swap) สำหรับชั้นถัดไป
        y = torch.cat([y2, x1], dim=1)
        return y, log_det

class FastFlowNet(nn.Module):
    def __init__(self, channels, num_blocks=8):
        super().__init__()
        # นำ Coupling Blocks มาต่อเรียงกัน 8 ชั้น
        self.blocks = nn.ModuleList([AffineCouplingBlock(channels) for _ in range(num_blocks)])

    def forward(self, x):
        log_det_total = 0
        for block in self.blocks:
            x, log_det = block(x)
            log_det_total += log_det
        return x, log_det_total

