def train_and_evaluate(model_name: str, backbone_name: str, train_loader: DataLoader, test_loader: DataLoader, config: dict) -> Dict:
    print(f"\n{'='*70}\n ▶ {model_name} + {backbone_name}\n{'='*70}")

    # ล้างค่าขยะก่อนเริ่มรันโมเดลใหม่
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()
        torch.cuda.reset_peak_memory_stats(DEVICE) # Reset สถิติ VRAM

    initial_ram = get_current_ram_usage()

    # ── สร้างโมเดล ──
    backbone = BackboneExtractor(backbone_name)
    model = build_model(model_name, backbone, config)

    # ── Training ──
    torch.cuda.synchronize() if DEVICE == "cuda" else None
    t_train_start = time.perf_counter()
    model.fit(train_loader)
    torch.cuda.synchronize() if DEVICE == "cuda" else None
    t_train_end = time.perf_counter()
    train_time_sec = t_train_end - t_train_start

    # ── Inference ──
    scores, y_true = [], []
    total_infer_time = 0.0
    for imgs, labels, _ in tqdm(test_loader, desc="  Inference", leave=False):
        imgs = imgs.to(DEVICE)
        torch.cuda.synchronize() if DEVICE == "cuda" else None
        t0 = time.perf_counter()
        score = model.predict(imgs)
        torch.cuda.synchronize() if DEVICE == "cuda" else None
        total_infer_time += (time.perf_counter() - t0)
        scores.append(score)
        y_true.append(int(labels[0].item()))

    infer_ms = (total_infer_time / max(len(test_loader.dataset), 1)) * 1000

    # วัด Peak Memory หลังจากรันเสร็จ
    peak_ram = get_current_ram_usage() # ค่า RAM สูงสุดที่บันทึกได้
    peak_vram = get_peak_vram_usage()

    metrics = compute_metrics(y_true, scores)
    metrics.update({
        "Model": model_name,
        "Backbone": backbone_name,
        "Train_Time_sec": round(train_time_sec, 2),
        "Inference_Time_per_Sample_ms": round(infer_ms, 3),
        "Peak_RAM_GB": round(peak_ram, 3),   # 🌟 ค่า RAM ระบบ
        "Peak_VRAM_GB": round(peak_vram, 3)   # 🌟 ค่า RAM การ์ดจอ
    })

    print(f"  ✅ AUROC={metrics['AUROC']:.4f} | RAM: {peak_ram:.2f}GB | VRAM: {peak_vram:.2f}GB")

    del model, backbone
    return metrics

