def display_anomaly_heatmap(img_path, heatmap, score, threshold, is_defect):
    # วาดรูป Overlay Heatmap
    original_img = cv2.imread(img_path)
    original_img = cv2.cvtColor(original_img, cv2.COLOR_BGR2RGB)
    original_img = cv2.resize(original_img, (IMAGE_SIZE, IMAGE_SIZE))

    # แปลง Heatmap เป็นแผนที่สี
    h_min, h_max = heatmap.min(), heatmap.max()
    norm_heatmap = (heatmap - h_min) / (h_max - h_min + 1e-10) # 0 to 1
    colored_heatmap = cv2.applyColorMap((norm_heatmap * 255).astype(np.uint8), cv2.COLORMAP_JET)
    colored_heatmap = cv2.cvtColor(colored_heatmap, cv2.COLOR_BGR2RGB)

    # ผสมภาพ
    overlay = cv2.addWeighted(original_img, 0.5, colored_heatmap, 0.5, 0)

    fig, axes = plt.subplots(1, 3, figsize=(15, 5))
    status = "🚨 DEFECT (Anomaly)" if score >= threshold else "✅ GOOD (Normal)"
    true_label = "Defect" if is_defect else "Good"
    fig.suptitle(f"Prediction: {status} | Actual: {true_label} | Score: {score:.2f}", fontsize=14, fontweight='bold')

    axes[0].imshow(original_img); axes[0].set_title("Original (PS Output)"); axes[0].axis('off')
    axes[1].imshow(norm_heatmap, cmap='jet'); axes[1].set_title("Anomaly Heatmap"); axes[1].axis('off')
    axes[2].imshow(overlay); axes[2].set_title("Overlay"); axes[2].axis('off')
    plt.tight_layout()
    plt.show()

print("📸 สุ่มแสดงภาพของเสียพร้อม Heatmap บอกพิกัดรอยแตก...")
# หาอินเด็กซ์ของภาพที่เป็นของเสีย
defect_indices = [i for i, y in enumerate(y_true) if y == 1]
if defect_indices:
    sample_idx = np.random.choice(defect_indices)
    display_anomaly_heatmap(img_paths_list[sample_idx], heatmaps[sample_idx], image_scores[sample_idx], best_threshold, True)

