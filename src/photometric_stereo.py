print(f"🎯 ใช้จุดตัด Threshold (Mean + {k}*Std): {threshold_val:.2f}")

_, defect_mask = cv2.threshold(curvature, threshold_val, 255, cv2.THRESH_BINARY)
defect_mask = defect_mask.astype(np.uint8)
