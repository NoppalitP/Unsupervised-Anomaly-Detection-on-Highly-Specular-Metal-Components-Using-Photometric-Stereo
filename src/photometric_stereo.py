    img_roi = img[int(cy-radius):int(cy+radius), int(cx-radius):int(cx+radius)]
    blurred_roi = cv2.GaussianBlur(img_roi, (5, 5), 0)

    # 2. Dynamic Threshold & Sub-pixel Moments
    _, max_val, _, _ = cv2.minMaxLoc(blurred_roi)
    thresh_val = max(50, max_val * 0.90)
    _, bright_mask = cv2.threshold(blurred_roi, thresh_val, 255, cv2.THRESH_BINARY)

    M = cv2.moments(bright_mask)
    if M["m00"] != 0:
        local_hx = M["m10"] / M["m00"]
        local_hy = M["m01"] / M["m00"]
    else:
        _, _, _, max_loc = cv2.minMaxLoc(blurred_roi)
        local_hx, local_hy = float(max_loc[0]), float(max_loc[1])

    hx = local_hx + int(cx - radius)
    hy = local_hy + int(cy - radius)

    # ------------------------------------------------
    # 🧮 PHYSICS MATH APPLIED HERE
    # ------------------------------------------------
    Nx = (hx - cx) / radius
    Ny = (cy - hy) / radius
    N_xy_sq = np.clip(Nx**2 + Ny**2, 0.0, 1.0)
    Nz = np.sqrt(1.0 - N_xy_sq)

    # L = 2(N.V)N - V
    L_vec = np.array([2 * Nx * Nz, 2 * Ny * Nz, 2 * Nz**2 - 1], dtype=np.float32)
    L_vec /= (np.linalg.norm(L_vec) + 1e-8) # Normalize
    light_directions.append(L_vec)

