class ImageFolderDataset(Dataset):
    def __init__(self, folder_path, transform):
        self.paths = sorted(glob.glob(os.path.join(folder_path, "*.png")) + glob.glob(os.path.join(folder_path, "*.jpg")))
        self.transform = transform
    def __len__(self): return len(self.paths)
    def __getitem__(self, idx): return self.transform(Image.open(self.paths[idx]).convert("RGB")), self.paths[idx]

def extract_features(folder_path, transform, label=None):
    dataset = ImageFolderDataset(folder_path, transform)
    if len(dataset) == 0: return np.array([]), np.array([]), []
    loader = DataLoader(dataset, batch_size=BATCH_SIZE, shuffle=False)

    feats, paths = [], []
    with torch.no_grad():
        for b_imgs, b_paths in loader:
            f = feature_extractor(b_imgs.to(device)).view(b_imgs.size(0), -1).cpu().numpy()
            feats.append(f)
            paths.extend(b_paths)
    return np.vstack(feats), np.full(len(paths), label) if label is not None else np.array([]), np.array(paths)

