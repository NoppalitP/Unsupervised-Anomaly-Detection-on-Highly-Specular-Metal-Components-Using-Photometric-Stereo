"""###Histogram Analysis

ในการสกัดหน้ากากชิ้นงาน (Object Mask) ขั้นตอนที่สำคัญที่สุดคือการแบ่งแยกพิกเซลของ **"ชิ้นงานโลหะ"** ออกจาก **"ฉากหลัง"**

### 1. ปัญหาของภาพดิบ (Raw Grayscale Histogram)
จากกราฟฮิสโตแกรมก่อนทำขบวนการเบลอ เราจะพบการกระจายตัวแบบภูเขา 2 ลูก (Bimodal Distribution):
* **ลูกซ้าย (ช่วงความสว่าง 10-20):** กลุ่มพิกเซลของฉากหลังที่มีความมืด
* **ลูกขวา (ช่วงความสว่าง 40-60):** กลุ่มพิกเซลของชิ้นงานโลหะที่สว่างกว่า
* **ปัญหาที่ซ่อนอยู่:** ช่วงหุบเขา (Valley) ระหว่างภูเขาทั้งสองลูกมีความ "หยักเป็นฟันปลา (High-frequency Noise)" สูงมาก ซึ่งเกิดจากลวดลายบนพื้นผิว (Texture) และสัญญาณรบกวนจากเซนเซอร์ ความขรุขระนี้มักทำให้อัลกอริทึมในการตัดขอบเขตทำงานผิดพลาดและเกิดหน้ากากที่มีรูโหว่

### 2. เวทมนตร์ของ Gaussian Blur
เมื่อเราประยุกต์ใช้ `cv2.GaussianBlur` ขนาดเคอร์เนล $5 \times 5$ พิกเซล กราฟฮิสโตแกรมจะเปลี่ยนไปในทิศทางที่อุดมคติมากขึ้น:
* ยอดภูเขาทั้งสองกลุ่มถูกเกลี่ยให้สมูทและรวมตัวกันแน่นขึ้น
* **หุบเขาตรงกลางลึกและเรียบเนียน (Smooth Valley):** ปราศจากรอยหยักโดยสิ้นเชิง สร้างพื้นที่ "รอยต่อ" ที่ชัดเจนสำหรับการหั่นข้อมูล

---

###  Otsu's Thresholding
เมื่อข้อมูลฮิสโตแกรมอยู่ในสภาพสมบูรณ์ เราจะใช้คำสั่ง `cv2.THRESH_OTSU` เพื่อให้ระบบค้นหาจุดตัดขอบเขต ($Threshold: T$) แบบอัตโนมัติ โดยไม่ต้องกำหนดค่าด้วยตัวเอง (Hardcode)

อัลกอริทึมของ Otsu จะทำการจำลองค่า $T$ ตั้งแต่ 0 ถึง 255 เพื่อค้นหาตำแหน่งที่ทำให้ **"ความแปรปรวนระหว่างกลุ่ม (Inter-class Variance)"** มีค่าสูงที่สุด (แปลว่าแยกสองกลุ่มออกจากกันได้ชัดเจนที่สุด) อ้างอิงตามสมการ:

$$\sigma_B^2 = \omega_0 \omega_1 (\mu_0 - \mu_1)^2$$

โดยที่:
* $\omega_0, \omega_1$ คือ สัดส่วนจำนวนพิกเซลของกลุ่มพื้นหลังและกลุ่มชิ้นงาน
* $\mu_0, \mu_1$ คือ ค่าเฉลี่ยความสว่างของแต่ละกลุ่ม

> **บทสรุป:** การทำ Gaussian Blur เพื่อปรับสมูทกราฟฮิสโตแกรม เป็นการสร้างสภาวะแวดล้อมที่เหมาะสมที่สุดให้สมการของ Otsu ทำงาน ส่งผลให้เราได้หน้ากากชิ้นงาน (Binary Mask) ที่ขอบเนียนกริบและแม่นยำ 100% พร้อมสำหรับการคำนวณ Photometric Stereo ในขั้นต่อไป
"""

_, mask = cv2.threshold(blurred, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)

imshow_colab(mask, title="Step 4: Otsu's Thresholding", cmap='gray')

"""###Morphological Clean-up

แม้ว่า Otsu's Thresholding จะแยกพิกเซลได้ดี แต่ในสภาพแวดล้อมจริง หน้ากากฐานสอง (Binary Mask) มักจะหลงเหลือสัญญาณรบกวน (Noise) 2 รูปแบบ คือ **"ฝุ่นบนฉากหลัง (False Positives)"** และ **"รูโหว่บนชิ้นงาน (False Negatives)"** เราจึงประยุกต์ใช้คณิตศาสตร์สัณฐานวิทยา (Morphological Operations) ด้วยตัวกระทำ (Structuring Element หรือ Kernel) ขนาด $5 \times 5$ พิกเซล เพื่อทำความสะอาดหน้ากากอย่างเด็ดขาดผ่าน 2 กระบวนการต่อเนื่อง:

### 1. การทำ Closing (อุดรูรั่วในชิ้นงาน)
เป็นการทำ Dilation (ขยาย) ตามด้วย Erosion (หด) อ้างอิงตามสมการเซต:
$$A \bullet B = (A \oplus B) \ominus B$$
**จุดประสงค์:** เพื่อถม "รูโหว่สีดำ" เล็กๆ ที่เกิดจากเงาหรือรอยขีดข่วนลึกบนชิ้นงานโลหะ ให้กลับมาเป็นพื้นที่สีขาวทึบสนิท

### 2. การทำ Opening (ลบจุดรบกวนบนฉากหลัง)
เป็นการทำ Erosion (หด) ตามด้วย Dilation (ขยาย) อ้างอิงตามสมการเซต:
$$A \circ B = (A \ominus B) \oplus B$$
**จุดประสงค์:** เพื่อกัดเซาะ "ติ่งสีขาว" หรือเศษฝุ่นขนาดเล็กที่ลอยอยู่บนฉากหลังสีดำให้หายไปอย่างถาวร โดยไม่กระทบกับขนาดและรูปทรงของชิ้นงานหลัก

> **ผลลัพธ์:** การใช้ `MORPH_CLOSE` ตามด้วย `MORPH_OPEN` เป็นเทคนิคมาตรฐานที่ช่วยการันตีว่าหน้ากากชิ้นงานจะมีความแข็งแกร่ง (Robust) ขอบเรียบเนียน และปราศจาก Noise กวนใจ 100%
"""

kernel = np.ones((5,5), np.uint8)
mask = cv2.morphologyEx(cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel), cv2.MORPH_OPEN, kernel)

imshow_colab(mask, title="Step 5: Morphological Clean-up", cmap='gray')

"""###Geometric Filtering & Contour Extraction

แม้จะผ่านการทำความสะอาดด้วยคณิตศาสตร์สัณฐานวิทยาแล้ว แต่ในสภาวะแวดล้อมจริงอาจมีวัตถุแปลกปลอมขนาดกลาง (เช่น เงาสะท้อนจากขอบโต๊ะ หรือชิ้นส่วนอื่นที่ติดเข้ามาในเฟรม) หลุดรอดมาได้ เพื่อการันตีความแม่นยำระดับ 100% เราจึงนำ **"ตรรกะเชิงพื้นที่ (Spatial Logic)"** เข้ามาเป็นตัวกรองชั้นสุดท้าย

### กระบวนการวิเคราะห์รูปทรง (Blob Analysis)
เราตั้งสมมติฐานทางกายภาพว่า **"ชิ้นงานเป้าหมาย จะต้องเป็นวัตถุที่มีพื้นที่เชื่อมต่อกันขนาดใหญ่ที่สุดในเฟรมภาพเสมอ"**

1. **Boundary Extraction:** ค้นหาเส้นโครงร่างชั้นนอกสุด (External Contours) ของทุกกลุ่มพิกเซลสีขาวในภาพ
2. **Area Maximization:** คำนวณพื้นที่ของแต่ละโครงร่าง และคัดกรองเก็บไว้เฉพาะโครงร่างที่มีค่า `cv2.contourArea` สูงที่สุด (Largest Connected Component)
3. **Mask Reconstruction:** สร้างอาร์เรย์หน้ากากใบใหม่ที่ว่างเปล่า และทำการถมพื้นที่ (Fill) ของโครงร่างที่ใหญ่ที่สุดลงไปด้วยสีขาวทึบ (ค่าพิกเซล 255)

> **บทสรุปของ Phase 2:** เราได้หน้ากากชิ้นงาน (Final Object Mask) ที่สมบูรณ์แบบ ไร้รอยรั่ว ไร้สัญญาณรบกวน ซึ่งจะทำหน้าที่เป็นกรอบจำกัดการคำนวณ (Region of Interest) ช่วยเพิ่มความเร็วและป้องกัน Error ให้กับสมการคณิตศาสตร์ 3 มิติในขั้นตอนต่อไป
"""

contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
final_mask = np.zeros_like(mask)
if contours:
    largest_contour = max(contours, key=cv2.contourArea)
    cv2.drawContours(final_mask, [largest_contour], -1, 255, thickness=cv2.FILLED)

