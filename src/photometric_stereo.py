# ทดสอบค่า n_estimators (จำนวนต้นไม้) และ contamination (สัดส่วนของเสียที่คาดการณ์)
n_estimators_list = [100, 200, 300]
max_samples_list = ['auto', 256] # จำนวนตัวอย่างที่ใช้สร้างแต่ละต้น

best_auroc = 0
